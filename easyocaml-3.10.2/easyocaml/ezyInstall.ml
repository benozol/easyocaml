(*
The "easyinstall" tool installs language levels and teachpacks.

Usage:

  easyinstall <options> FILE

  where options are:
  -remove (remove the level or teachpack specified)
  -update (update the level or teachpack specified. Per default,
           easyinstall refuses to overwrite an existing level
	   or teachpack)
  -name NAME (default: basename of FILE without extension)

*)